package jp.co.scsk.kyushu.calcSample;

public class CalcSmaple01 {

	public static void main(String[] args) {
		int a = 5;
		int b = 2;

		// 加算
		System.out.println("(加算) a + b = " + (a + b));
		// 減算
		System.out.println("(減算) a - b = " + (a - b));
		// 乗算
		System.out.println("(乗算) a * b = " + (a * b));
		// 除算（商）
		System.out.println("(除算(商)) a / b = " + (a / b));
		// 除算（余）
		System.out.println("(除算(余)) a % b = " + (a % b));
	}

}
